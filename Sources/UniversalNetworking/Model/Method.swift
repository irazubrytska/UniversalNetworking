/// Enum that represents an HTTP method.
public enum Method: String {

    /// Get method.
    case get = "GET"

    /// Post method.
    case post = "POST"

    /// Patch method.
    case patch = "PATCH"

    /// Delete method.
    case delete = "DELETE"

    /// Put method.
    case put = "PUT"
}
